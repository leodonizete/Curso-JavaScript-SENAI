// Crie um programa que receba um index (0, 1, 2, etc.) e retorne qual o dia da semana.
// (0 = domingo, 1 = segunda, 2 = terça, 3 = quarta, 4 = quinta, 5 = sexta, 6 = sábado)

let index = 0

if (index == 0) {
    console.log('Domingo')
} else if (index == 1) {
    console.log('Segunda')
} else if (index == 2) {
    console.log('Terça')
} else if (index == 3) {
    console.log('Quarta')
} else if (index == 4) {
    console.log('Quinta')
} else if (index == 5) {
    console.log('Sexta')
} else if (index == 6) {
    console.log('Sábado')
}